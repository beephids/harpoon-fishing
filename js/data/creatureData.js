// Frame helper: row index -> source rectangles for both animation frames
function frames(row) {
    return [
        { sx: 0, sy: row * 48, sw: 48, sh: 48 },
        { sx: 48, sy: row * 48, sw: 48, sh: 48 },
    ];
}

